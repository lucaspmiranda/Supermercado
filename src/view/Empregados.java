    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTEmpregados.getModel();
        modelo.setNumRows(0);
        EmpregadoDAO pdao = new EmpregadoDAO();
        pdao.read().forEach((e) -> {
            modelo.addRow(new Object[]{
                e.getId(),
                e.getEmpregado(),
                e.getCPF(),
                e.getCargo()
            });
        });
    }

    public void readJTableForDesc(String desc) {
        DefaultTableModel modelo = (DefaultTableModel) jTEmpregados.getModel();
        modelo.setNumRows(0);
        EmpregadoDAO pdao = new EmpregadoDAO();
        pdao.readForDesc(desc).forEach((e) -> {
            modelo.addRow(new Object[]{
                e.getId(),
                e.getEmpregado(),
                e.getCPF(),
                e.getCargo()
            });
        });
    }

